public class Calculadora {

    public static int soma (int a, int b){
        return (a+b);
    }
    public static float soma(float n1, float n2){
        return (n1+ n2);
    }
    public static float soma(float n1, float n2, float n3){
        return (n1+ n2 + n3);
    }
    public static float soma(float n1, float n2, float n3, float n4){
        return (n1+ n2+ n3 + n4);
    }
}
